#################  61.sum()

'''

The sum() function returns a number, the sum of all items in an iterable.

# Syntax: 
sum(iterable, start)

iterable	----    Required. The sequence to sum
start	    ----    Optional. A value that is added to the return value

'''

# Ex-1: Add all items in a tuple, and return the result:

a = (1, 2, 3, 4, 5)
x = sum(a)      # 15

# Ex-2: Start with the number 7, and add all the items in a tuple to this number:

a = (1, 2, 3, 4, 5)
x = sum(a, 7)   # 22



