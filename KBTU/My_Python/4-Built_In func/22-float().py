#########  21.float()

'''
The float() function converts the specified value into a floating point number.

# Syntax:
float(value)

value	----    A number or a string that can be converted into a floating point number

'''

'''
*********************************************************************************************************************
'''

# Ex-1: Convert the number 3 into a floating point number:

x = float(3)    # 3.0

# Ex-2: Convert a string into a floating point number:

x = float("3.500")  # 3.5