###################   47.ord()

'''
The ord() function returns the number representing the unicode code of a specified character.

# Syntax: 

ord(character)

character	----    String, any character

'''

'''
*********************************************************************************************************************
'''

# Ex-1: Return the integer that represents the character "A,Z,a,z":

print(ord('A')) # 65
print(ord('Z')) # 90
print(ord('a')) # 97
print(ord('z')) # 122
print(chr(122)) # z