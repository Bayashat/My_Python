# You can't access items in a set by index or key
# But you can loop through the set items using for loop, or ask a specified value is present in a set, by using "in" keyword

                ##### 1. Example :
# Loop through the set, and print value:
my_set = {"apple", "banana", "Cherry"}

for i in my_set:
    print(i)        # banana cherry apple


            ##### 2. Example :
# check if "banana" is present in our set:
print("Limon" in my_set)   # True