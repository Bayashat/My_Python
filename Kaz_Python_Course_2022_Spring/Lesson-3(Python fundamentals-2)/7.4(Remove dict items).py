#####        1.pop()  : dict.pop(key) : will returns the value
my_dict = { "brand" : "Mercedes", "Model" : "G Class", "year" : 1964 }

my_dict.pop("Model")
print(my_dict)

############   2. popitem() : removes the last inserted item
my_dict = { "brand" : "Mercedes", "Model" : "G Class", "year" : 1964 }
my_dict.popitem()
print(my_dict)

