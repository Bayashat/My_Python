import math
# Python has a set of built-in functions can allows you to perform mathematical tasks on numbers

                # <1> Built-in Math function
#1) Min() Max()

x = min(5,10,25)
y = max(5,10,25)
print(x, y)

#2) abs() : 
print(abs(-17.5))

# 3) pow(x,y)
x = pow(3,4) 
print(x) # 81


                ### <2> Math module
# sqrt:
x = math.sqrt(64)
print(x)

# ceil
# floor
print(math.ceil(2.1)) # 3
print(math.floor(2.9)) # 2

# math.PI

print(math.pi)

